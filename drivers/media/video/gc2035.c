static int gc2035_probe(struct i2c_client *client, const struct i2c_device_id *id)
{
    struct gc2035_priv *priv;
    struct v4l2_subdev *sd;
    int ret;
    printk("%s .............\n",__func__);
    priv = kzalloc(sizeof(struct gc2035_priv), GFP_KERNEL);
    if (!priv)
        return -ENOMEM;

    gc2035_priv_init(priv);

    sd = &priv->subdev;
    strcpy(sd->name, MODULE_NAME);

    /* register subdev */
    v4l2_i2c_subdev_init(sd, client, &gc2035_subdev_ops);

    sd->flags |= V4L2_SUBDEV_FL_HAS_DEVNODE;
    priv->pad.flags = MEDIA_PAD_FL_SOURCE;
    sd->entity.type = MEDIA_ENT_T_V4L2_SUBDEV_SENSOR;
    sd->entity.ops  = &gc2035_media_ops;
    if (media_entity_init(&sd->entity, 1, &priv->pad, 0)) {
        dev_err(&client->dev, "%s: failed to media_entity_init()\n", __func__);
        kfree(priv);
        return -ENOENT;
    }

    ret = gc2035_initialize_ctrls(priv);
    if (ret < 0) {
        printk(KERN_ERR "%s: failed to initialize controls\n", __func__);
        kfree(priv);
        return ret;
    }

    check_id(client);

    return 0;
}

