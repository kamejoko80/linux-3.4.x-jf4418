//#define DBG_IIC  // to show wether regisers are written correctly
static int gc2035_write_array(struct i2c_client *client, const struct regval_list *vals)
{
    int ret;
    while (vals->reg_num != 0xff) {
        ret = i2c_smbus_write_byte_data(client, vals->reg_num, vals->value);
        if (ret < 0)
            return ret;
#ifdef DBG_IIC
        printk("REG[%02x],DATA[%02x]\n",vals->reg_num,i2c_smbus_read_byte_data(client,vals->reg_num));
#endif 


        vals++;
    }
    return 0;
}

static int gc2035_mask_set(struct i2c_client *client, u8 command, u8 mask, u8 set) __attribute__((unused));
static int gc2035_mask_set(struct i2c_client *client, u8 command, u8 mask, u8 set)
{
    s32 val = i2c_smbus_read_byte_data(client, command);
    if (val < 0)
        return val;

    val &= ~mask;
    val |= set & mask;

    return i2c_smbus_write_byte_data(client, command, val);
}

static const struct gc2035_win_size *gc2035_select_win(u32 width, u32 height)
{
	const struct gc2035_win_size *win;
    int i;
    printk("%s .............\n",__func__);

    for (i = 0; i < ARRAY_SIZE(gc2035_win); i++) {
        win = gc2035_win[i];
        if (width == win->width && height == win->height)
            return win;
    }

    printk(KERN_ERR "%s: unsupported width, height (%dx%d)\n", __func__, width, height);
    return NULL;
}

static int gc2035_set_mbusformat(struct i2c_client *client, const struct gc2035_color_format *cfmt)
{
    enum v4l2_mbus_pixelcode code;
    int ret = -1;
    code = cfmt->code;
    printk("%s .............\n",__func__);
    switch (code) {
        case V4L2_MBUS_FMT_YUYV8_2X8:
            ret  = gc2035_write_array(client, gc2035_fmt_yuv422_yuyv);
            break;
        case V4L2_MBUS_FMT_UYVY8_2X8:
            ret  = gc2035_write_array(client, gc2035_fmt_yuv422_uyvy);
            break;
        case V4L2_MBUS_FMT_YVYU8_2X8:
            ret  = gc2035_write_array(client, gc2035_fmt_yuv422_yvyu);
            break;
        case V4L2_MBUS_FMT_VYUY8_2X8:
            ret  = gc2035_write_array(client, gc2035_fmt_yuv422_vyuy);
            break;
        default:
            printk(KERN_ERR "mbus code error in %s() line %d\n",__FUNCTION__, __LINE__);
    }
    return ret;
}

static int gc2035_set_params(struct v4l2_subdev *sd, u32 *width, u32 *height, enum v4l2_mbus_pixelcode code)
{
    struct gc2035_priv *priv = to_priv(sd);
    const struct gc2035_win_size *old_win, *new_win;
    int i;
    printk("%s .............\n",__func__);
    /*
     * select format
     */
    priv->cfmt = NULL;
    for (i = 0; i < ARRAY_SIZE(gc2035_cfmts); i++) {
        if (code == gc2035_cfmts[i].code) {
            priv->cfmt = gc2035_cfmts + i;
            break;
        }
    }
    if (!priv->cfmt) {
        printk(KERN_ERR "Unsupported sensor format.\n");
        return -EINVAL;
    }

    /*
     * select win
     */
    old_win = priv->win;
    new_win = gc2035_select_win(*width, *height);
    if (!new_win) {
        printk(KERN_ERR "Unsupported win size\n");
        return -EINVAL;
    }
    priv->win = new_win;

    priv->rect.left = 0;
    priv->rect.top = 0;
    priv->rect.width = priv->win->width;
    priv->rect.height = priv->win->height;

    *width = priv->win->width;
    *height = priv->win->height;

    return 0;
}

