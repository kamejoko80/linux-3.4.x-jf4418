/**
 * video ops
 */
static int gc2035_s_stream(struct v4l2_subdev *sd, int enable)
{
    struct i2c_client *client = v4l2_get_subdevdata(sd);
    struct gc2035_priv *priv = to_priv(sd);
    int ret = 0;

    printk("%s: enable %d, initialized %d\n", __func__, enable, priv->initialized);
    if (enable) {
        if (!priv->win || !priv->cfmt) {
            dev_err(&client->dev, "norm or win select error\n");
            return -EPERM;
        }
         /* write init regs */
        if (!priv->initialized) {
            if (!check_id(client))
                return -EINVAL;

            ret = gc2035_write_array(client, gc2035_svga_init_regs);
            printk("gc2035 ready to set regisers...........\n");
            if (ret < 0) {
                printk(KERN_ERR "%s: failed to gc2035_write_array init regs\n", __func__);
                return -EIO;
            }
            priv->initialized = true;
        }

        ret = gc2035_write_array(client, priv->win->win_regs);
        if (ret < 0) {
            printk(KERN_ERR "%s: failed to gc2035_write_array win regs\n", __func__);
            return -EIO;
        }

        ret = gc2035_set_mbusformat(client, priv->cfmt);
        if (ret < 0) {
            printk(KERN_ERR "%s: failed to gc2035_set_mbusformat()\n", __func__);
            return -EIO;
        }
    } else {
        gc2035_write_array(client, gc2035_disable_regs);
    }

    return ret;
}

static int gc2035_enum_framesizes(struct v4l2_subdev *sd, struct v4l2_frmsizeenum *fsize)
{
    struct i2c_client *client = v4l2_get_subdevdata(sd);
    printk("%s .............\n",__func__);
    if (fsize->index >= ARRAY_SIZE(gc2035_win)) {
        dev_err(&client->dev, "index(%d) is over range %d\n", fsize->index, ARRAY_SIZE(gc2035_win));
        return -EINVAL;
    }

    switch (fsize->pixel_format) {
        case V4L2_PIX_FMT_YUV420:
        case V4L2_PIX_FMT_YUV422P:
        case V4L2_PIX_FMT_NV12:
        case V4L2_PIX_FMT_YUYV:
            fsize->type = V4L2_FRMSIZE_TYPE_DISCRETE;
            fsize->discrete.width = gc2035_win[fsize->index]->width;
            fsize->discrete.height = gc2035_win[fsize->index]->height;
            break;
        default:
            dev_err(&client->dev, "pixel_format(%d) is Unsupported\n", fsize->pixel_format);
            return -EINVAL;
    }

    dev_info(&client->dev, "type %d, width %d, height %d\n", V4L2_FRMSIZE_TYPE_DISCRETE, fsize->discrete.width, fsize->discrete.height);
    return 0;
}

