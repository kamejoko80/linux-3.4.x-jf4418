static int gc2035_try_mbus_fmt(struct v4l2_subdev *sd,
        struct v4l2_mbus_framefmt *mf)
{
    /* struct i2c_client *client = v4l2_get_subdevdata(sd); */
    struct gc2035_priv *priv = to_priv(sd);
    const struct gc2035_win_size *win;
    int i;
    printk("%s .............\n",__func__);
    /*
     * select suitable win
     */
    win = gc2035_select_win(mf->width, mf->height);
    if (!win)
        return -EINVAL;

    mf->width   = win->width;
    mf->height  = win->height;
    mf->field   = V4L2_FIELD_NONE;


    for (i = 0; i < ARRAY_SIZE(gc2035_cfmts); i++)
        if (mf->code == gc2035_cfmts[i].code)
            break;

    if (i == ARRAY_SIZE(gc2035_cfmts)) {
        /* Unsupported format requested. Propose either */
        if (priv->cfmt) {
            /* the current one or */
            mf->colorspace = priv->cfmt->colorspace;
            mf->code = priv->cfmt->code;
        } else {
            /* the default one */
            mf->colorspace = gc2035_cfmts[0].colorspace;
            mf->code = gc2035_cfmts[0].code;
        }
    } else {
        /* Also return the colorspace */
        mf->colorspace	= gc2035_cfmts[i].colorspace;
    }

    return 0;
}

